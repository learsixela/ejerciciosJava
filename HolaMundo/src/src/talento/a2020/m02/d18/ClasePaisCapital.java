package src.talento.a2020.m02.d18;

public class ClasePaisCapital {
	String Capital, Pais;
	public ClasePaisCapital() {
		
	}
	public ClasePaisCapital(String capital, String pais) {
		super();
		Capital = capital;
		Pais = pais;
	}
	
	public String getCapital() {
		return Capital;
	}
	public void setCapital(String capital) {
		Capital = capital;
	}
	public String getPais() {
		return Pais;
	}
	public void setPais(String pais) {
		Pais = pais;
	}


}
